char *upr(const char *input)
{
    int len = strlen(input);
    char *output = (char *)malloc(len + 1);
    
    for (int i = 0; i < len; i++)
    {
        output[i] = toupper(input[i]);
    }
    output[len] = '\0';
    
    return output;
}

